package muveletek;

public class muveletek {

    public static int osszead(int a, int b) {
        return a + b;
    }

    public static int kivon(int a, int b) {
        return a - b;
    }

    public static double oszt(double a, double b) {
        return a / b;
    }

    public static int szoroz(int a, int b) {
        return a * b;
    }


}
